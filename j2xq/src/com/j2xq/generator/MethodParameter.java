package com.j2xq.generator;

/**
 * Holds a function parameter
 * @author soumadri
 *
 */
public class MethodParameter {
	String type,value;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
}
